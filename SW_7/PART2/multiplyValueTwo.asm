; DOMINIC GAIERO AND ELIZABETH DAVIS
;
; DESCRIPTION:
; MULTIPLIES TWO 8 BIT NUMBERS TOGETHER AND SEPERATES THE
; EIGHTEEN BIT RESULT INTO A HIGH 8 BITS AND A LOW 8 BITS
; ----------------------------------------------------
.CSEG
.ORG 0x20


MAIN:
MOV R2, 0x00
MOV R3, 0x00
IN R0, 0x20
CMP R0, 0x00
BREQ OUTPUT_ZERO_VAL
IN R1, 0x21
CMP R1, 0x00
BREQ OUTPUT_ZERO_VAL
MOV R2, R1
MULTIPLY: CALL MULTIPLY_VAL
SUB R2, 0x01
BREQ OUTPUT_VAL
BRN MULTIPLY

OUTPUT_VAL: OUT R3, 0x41
OUT R4, 0x42
BRN 0x50

OUTPUT_ZERO_VAL: MOV R3, 0x00
MOV R0, 0x00
OUT R3, 0x41
OUT R0, 0x42
BRN 0x50

MULTIPLY_VAL: ADD R4,R0
ADDC R3, 0x00
RET
