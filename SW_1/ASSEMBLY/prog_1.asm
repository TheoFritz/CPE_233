;--------------------------------------------------------------------------
;-- .ORG used in code segment
;--------------------------------------------------------------------------
.CSEG
.ORG 0x34 ; sets the code segment counter to 0x34
  IN 0x30
  OUT 0x40
;--------------------------------------------------------------------------
;-- Register Aliases
;--------------------------------------------------------------------------
.DEF R_IN = R11

;--------------------------------------------------------------------------
;-- .ORG used in data segment
;--------------------------------------------------------------------------
.DSEG
.ORG 0x5A ; set the data segment counter to 0x5A
MOV R40,0x00
IN R30
ADD R40,R30
